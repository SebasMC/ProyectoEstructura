    private void jPistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPistaActionPerformed
        Vuelo temp;
        Nodo vp;
        try {
            vp = MainVuelos.terminales[Integer.parseInt((String) jTerminal.getSelectedItem()) - 1].elimina((String) jVuelo.getSelectedItem());
            //temp = (Vuelo) MainVuelos.terminales[Integer.parseInt((String) jTerminal.getSelectedItem()) - 1].elimina((String) jVuelo.getSelectedItem()).getObj();
            temp = (Vuelo) vp.getObj();
            MainVuelos.pista.insertar(vp);
            jVuelo.removeItem(jVuelo.getSelectedItem());
            JOptionPane.showMessageDialog(null, "Vuelo " + temp.getNum_vuelo() + " de la terminal " + temp.getNum_t() + " ingrasado a la pista");
        } catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "No hay ning√∫n vuelo por ingresar a la pista", "ERROR!!!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jPistaActionPerformed

