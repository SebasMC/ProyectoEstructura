    private void jTerminalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTerminalActionPerformed
        pos = Integer.parseInt((String) jTerminal.getSelectedItem()) - 1;
        try {
            jVuelo.removeAllItems();
            Manipula.cargaCombo(MainVuelos.terminales[pos].getR(), jVuelo);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "No hay vuelos en esta terminal", "", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jTerminalActionPerformed

